from framebuf import MONO_HLSB, FrameBuffer


class ImageDisplay:
    def __init__(
        self,
        width: int,
        height: int,
        array: bytearray,
        format=MONO_HLSB,
    ) -> None:
        self.width = width
        self.height = height
        self.array = array
        self.image = FrameBuffer(array, width, height, format)

    def center_width(self, display_width: int) -> int:
        return (display_width - self.width) // 2

    def center_height(self, display_height: int) -> int:
        return (display_height - self.height) // 2


